#include<stdio.h>

int main() {
    int n;
    scanf("%d", &n);
    if (n == 0)
        puts("zero");

    if (n == 1)
        puts("one");

    if (n == 2)
        puts("two");

    if (n == 3)
        puts("three");

    if (n == 4)
        puts("four");

    if (n == 5)
        puts("five");

    if (n == 6)
        puts("six");

    if (n == 7)
        puts("seven");

    if (n == 8)
        puts("eight");

    if (n == 9)
        puts("nine");

    if (n > 9)
        puts("Greater than 9");
}
